#!/usr/bin/env python3
"""
summarize_results.py — Aggregate all pipeline outputs into a single report TSV.

Collects:
  - Assembly statistics (size, N50, contig count)
  - BUSCO completeness scores (C, D, F, M percentages)
  - Repeatome composition per sample (% genome per TE superfamily)
  - ToxA BLAST hit presence/absence, coordinates, and % identity
  - Y-Recombinase (Starship) hit presence/absence and copy number
  - TSD sequences detected

This script is called by both the `compare_starship_structure` rule and the
`aggregate_report` rule in visualization.smk.

Usage:
    python summarize_results.py \\
        --assembly-stats results/assembly_stats/*.txt \\
        --busco results/busco/*/short_summary*.txt \\
        --repeats results/edta/*/*.tsv \\
        --blast results/blast/*/*_toxa_hits.tsv \\
        --yrec results/starship/*/*.tblout \\
        --output results/report/summary_report.tsv
"""

import argparse
import re
import sys
from pathlib import Path
from collections import defaultdict

import pandas as pd


# ─── Assembly stats parser ────────────────────────────────────────────────────
def parse_assembly_stats(stat_files: list[str]) -> pd.DataFrame:
    """
    Parse assembly-stats output (plain text table) into a DataFrame.
    assembly-stats writes a tab-separated table with a header row.
    """
    rows = []
    for fpath in stat_files:
        sample = Path(fpath).stem.replace("_stats", "")
        try:
            with open(fpath) as fh:
                lines = [l.strip() for l in fh if l.strip() and not l.startswith("filename")]
            for line in lines:
                fields = line.split("\t")
                if len(fields) >= 10:
                    rows.append({
                        "sample":        sample,
                        "total_length":  int(fields[1]),
                        "num_seqs":      int(fields[2]),
                        "N50":           int(fields[4]),
                        "L50":           int(fields[5]),
                        "N90":           int(fields[6]),
                        "largest_contig": int(fields[3]),
                    })
        except Exception as exc:
            print(f"WARNING: Could not parse {fpath}: {exc}", file=sys.stderr)
    return pd.DataFrame(rows)


# ─── BUSCO parser ─────────────────────────────────────────────────────────────
def parse_busco_summaries(busco_files: list[str]) -> pd.DataFrame:
    """
    Parse BUSCO short summary text files.

    The summary file contains lines like:
        C:98.5%[S:96.2%,D:2.3%],F:0.5%,M:1.0%,n:758
    """
    rows = []
    pattern = re.compile(
        r"C:([0-9.]+)%\[S:([0-9.]+)%,D:([0-9.]+)%\],F:([0-9.]+)%,M:([0-9.]+)%,n:(\d+)"
    )
    for fpath in busco_files:
        # Extract sample name from file path
        sample = Path(fpath).stem.split(".")[-1]  # last component after dots
        try:
            text = Path(fpath).read_text()
            m = pattern.search(text)
            if m:
                rows.append({
                    "sample":            sample,
                    "busco_complete":    float(m.group(1)),
                    "busco_single":      float(m.group(2)),
                    "busco_duplicated":  float(m.group(3)),
                    "busco_fragmented":  float(m.group(4)),
                    "busco_missing":     float(m.group(5)),
                    "busco_n_orthologs": int(m.group(6)),
                })
            else:
                print(f"WARNING: BUSCO pattern not found in {fpath}", file=sys.stderr)
        except Exception as exc:
            print(f"WARNING: Could not parse {fpath}: {exc}", file=sys.stderr)
    return pd.DataFrame(rows)


# ─── Repeat summary parser ────────────────────────────────────────────────────
def parse_repeat_summaries(repeat_files: list[str]) -> pd.DataFrame:
    """
    Parse per-sample EDTA repeat summary TSVs (generated by parse_edta_summary.py).
    Expected columns: Sample, TE_Superfamily, Total_bp, Genome_Fraction_pct
    """
    dfs = []
    for fpath in repeat_files:
        try:
            df = pd.read_csv(fpath, sep="\t")
            dfs.append(df)
        except Exception as exc:
            print(f"WARNING: Could not parse {fpath}: {exc}", file=sys.stderr)
    if not dfs:
        return pd.DataFrame()
    combined = pd.concat(dfs, ignore_index=True)
    # Pivot to wide format: one row per sample, columns per TE superfamily
    if "TE_Superfamily" in combined.columns and "Genome_Fraction_pct" in combined.columns:
        wide = combined.pivot_table(
            index="Sample",
            columns="TE_Superfamily",
            values="Genome_Fraction_pct",
            aggfunc="sum",
        ).reset_index()
        wide.columns.name = None
        wide = wide.rename(columns={"Sample": "sample"})
        return wide
    return combined


# ─── BLAST hits parser ────────────────────────────────────────────────────────
def parse_blast_hits(blast_files: list[str]) -> pd.DataFrame:
    """
    Parse ToxA tblastn hit files (BLAST outfmt 6).
    Returns presence/absence + best hit metrics per sample.
    """
    rows = []
    col_names = ["qseqid", "sseqid", "pident", "length", "mismatch",
                 "gapopen", "qstart", "qend", "sstart", "send", "evalue", "bitscore"]
    for fpath in blast_files:
        parts = Path(fpath).parts
        # Extract sample from path: results/blast/<sample>/<sample>_toxa_hits.tsv
        sample = Path(fpath).stem.replace("_toxa_hits", "")
        try:
            hits = []
            with open(fpath) as fh:
                for line in fh:
                    if line.strip() and not line.startswith("#"):
                        fields = line.strip().split("\t")
                        if len(fields) >= len(col_names):
                            hits.append(dict(zip(col_names, fields)))
            if hits:
                best = max(hits, key=lambda h: float(h["bitscore"]))
                rows.append({
                    "sample":           sample,
                    "toxa_present":     True,
                    "toxa_n_hits":      len(hits),
                    "toxa_best_contig": best["sseqid"],
                    "toxa_pct_identity": float(best["pident"]),
                    "toxa_evalue":      float(best["evalue"]),
                    "toxa_bitscore":    float(best["bitscore"]),
                })
            else:
                rows.append({"sample": sample, "toxa_present": False, "toxa_n_hits": 0})
        except Exception as exc:
            print(f"WARNING: Could not parse {fpath}: {exc}", file=sys.stderr)
    return pd.DataFrame(rows)


# ─── Y-Rec hits parser ────────────────────────────────────────────────────────
def parse_yrec_hits(yrec_files: list[str]) -> pd.DataFrame:
    """
    Parse HMMER tblout files for Y-Recombinase (DUF3435) hits.
    Returns presence/absence + copy count per sample.
    """
    rows = []
    for fpath in yrec_files:
        sample = Path(fpath).stem.replace("_yrec_hits", "")
        try:
            n_hits = 0
            with open(fpath) as fh:
                for line in fh:
                    if not line.startswith("#") and line.strip():
                        n_hits += 1
            rows.append({
                "sample":          sample,
                "yrec_present":    n_hits > 0,
                "yrec_copy_count": n_hits,
            })
        except Exception as exc:
            print(f"WARNING: Could not parse {fpath}: {exc}", file=sys.stderr)
    return pd.DataFrame(rows)


# ─── Main ─────────────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(description="Aggregate pipeline results.")
    parser.add_argument("--assembly-stats", nargs="*", default=[], metavar="FILE")
    parser.add_argument("--busco",          nargs="*", default=[], metavar="FILE")
    parser.add_argument("--repeats",        nargs="*", default=[], metavar="FILE")
    parser.add_argument("--blast",          nargs="*", default=[], metavar="FILE")
    parser.add_argument("--yrec",           nargs="*", default=[], metavar="FILE")
    # Legacy args from starship_analysis.smk compare rule
    parser.add_argument("--yrec-hits",      nargs="*", default=[], metavar="FILE")
    parser.add_argument("--tsds",           nargs="*", default=[], metavar="FILE")
    parser.add_argument("--blast-hits",     nargs="*", default=[], metavar="FILE")
    parser.add_argument("--samples",        nargs="*", default=[], metavar="SAMPLE")
    parser.add_argument("--output",         required=True, help="Output TSV path")
    args = parser.parse_args()

    # Merge legacy argument aliases
    yrec_files  = (args.yrec or []) + (args.yrec_hits or [])
    blast_files = (args.blast or []) + (args.blast_hits or [])

    # ── Parse all sources ────────────────────────────────────────────────────
    dfs = {}

    if args.assembly_stats:
        print("Parsing assembly stats...", file=sys.stderr)
        dfs["assembly"] = parse_assembly_stats(args.assembly_stats)

    if args.busco:
        print("Parsing BUSCO summaries...", file=sys.stderr)
        dfs["busco"] = parse_busco_summaries(args.busco)

    if args.repeats:
        print("Parsing repeat summaries...", file=sys.stderr)
        dfs["repeats"] = parse_repeat_summaries(args.repeats)

    if blast_files:
        print("Parsing BLAST hits...", file=sys.stderr)
        dfs["blast"] = parse_blast_hits(blast_files)

    if yrec_files:
        print("Parsing Y-Rec HMM hits...", file=sys.stderr)
        dfs["yrec"] = parse_yrec_hits(yrec_files)

    # ── Merge on 'sample' column ─────────────────────────────────────────────
    if not dfs:
        print("WARNING: No input files parsed. Writing empty report.", file=sys.stderr)
        Path(args.output).parent.mkdir(parents=True, exist_ok=True)
        pd.DataFrame(columns=["sample"]).to_csv(args.output, sep="\t", index=False)
        return

    merged = None
    for key, df in dfs.items():
        if df.empty:
            continue
        if merged is None:
            merged = df
        else:
            merged = merged.merge(df, on="sample", how="outer")

    if merged is None or merged.empty:
        print("WARNING: All DataFrames were empty. Writing empty report.", file=sys.stderr)
        Path(args.output).parent.mkdir(parents=True, exist_ok=True)
        pd.DataFrame(columns=["sample"]).to_csv(args.output, sep="\t", index=False)
        return

    merged = merged.sort_values("sample")

    Path(args.output).parent.mkdir(parents=True, exist_ok=True)
    merged.to_csv(args.output, sep="\t", index=False)
    print(f"Summary report: {len(merged)} samples × {len(merged.columns)} columns",
          file=sys.stderr)
    print(f"Written to: {args.output}", file=sys.stderr)

    # ── Print to stdout for quick inspection ─────────────────────────────────
    print(merged.to_string(index=False))


if __name__ == "__main__":
    main()
